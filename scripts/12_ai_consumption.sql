-- PART 4: AI FEEDBACK LOOP
-- Write insights back to source Postgres for operational use
-- ============================================================================

-- Table to stage AI-generated insights (would sync back to Postgres)
CREATE OR REPLACE TABLE GOLD.AI_CUSTOMER_INSIGHTS (
    insight_id VARCHAR DEFAULT UUID_STRING(),
    customer_id INTEGER,
    insight_type VARCHAR,           -- 'CHURN_RISK', 'UPSELL_OPPORTUNITY', 'ENGAGEMENT_ALERT'
    insight_severity VARCHAR,       -- 'HIGH', 'MEDIUM', 'LOW'
    recommendation TEXT,
    supporting_data VARIANT,
    generated_by VARCHAR DEFAULT 'CORTEX_AGENT',
    generated_at TIMESTAMP_LTZ DEFAULT CURRENT_TIMESTAMP(),
    synced_to_postgres BOOLEAN DEFAULT FALSE,
    actioned BOOLEAN DEFAULT FALSE
);

-- Procedure to generate insights from Gold data
CREATE OR REPLACE PROCEDURE AGENTS.GENERATE_CUSTOMER_INSIGHTS()
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
BEGIN
    -- Churn Risk Insights
    INSERT INTO GOLD.AI_CUSTOMER_INSIGHTS (customer_id, insight_type, insight_severity, recommendation, supporting_data)
    SELECT 
        customer_id,
        'CHURN_RISK',
        CASE 
            WHEN days_since_last_order > 90 AND lifetime_value > 50000 THEN 'HIGH'
            WHEN days_since_last_order > 60 THEN 'MEDIUM'
            ELSE 'LOW'
        END,
        CASE 
            WHEN days_since_last_order > 90 THEN 'URGENT: Schedule executive call - high value customer at risk'
            WHEN days_since_last_order > 60 THEN 'Send personalized re-engagement email with special offer'
            ELSE 'Monitor - proactive check-in recommended'
        END,
        OBJECT_CONSTRUCT(
            'days_inactive', days_since_last_order,
            'lifetime_value', lifetime_value,
            'segment', segment
        )
    FROM GOLD.PG_CUSTOMER_360
    WHERE engagement_status IN ('AT_RISK', 'COOLING')
      AND customer_id NOT IN (SELECT customer_id FROM GOLD.AI_CUSTOMER_INSIGHTS WHERE insight_type = 'CHURN_RISK' AND generated_at > DATEADD('day', -7, CURRENT_TIMESTAMP()));
    
    -- Upsell Opportunities
    INSERT INTO GOLD.AI_CUSTOMER_INSIGHTS (customer_id, insight_type, insight_severity, recommendation, supporting_data)
    SELECT 
        customer_id,
        'UPSELL_OPPORTUNITY',
        'MEDIUM',
        'Customer shows growth potential - recommend premium tier upgrade',
        OBJECT_CONSTRUCT(
            'current_tier', revenue_tier,
            'order_frequency', total_orders,
            'avg_order_value', avg_order_value
        )
    FROM GOLD.PG_CUSTOMER_360
    WHERE engagement_status = 'HOT'
      AND revenue_tier = 'SMB'
      AND total_orders >= 3
      AND customer_id NOT IN (SELECT customer_id FROM GOLD.AI_CUSTOMER_INSIGHTS WHERE insight_type = 'UPSELL_OPPORTUNITY' AND generated_at > DATEADD('day', -30, CURRENT_TIMESTAMP()));
    
    RETURN 'Insights generated successfully';
END;
$$;

