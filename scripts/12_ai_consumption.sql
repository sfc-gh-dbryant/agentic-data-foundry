AS SELECT * FROM GOLD.PG_CUSTOMER_360
COMMENT = 'Customer analytics with lifetime value, engagement metrics, and churn indicators'
WITH SEMANTIC MODEL (
    tables:
      - name: PG_CUSTOMER_360
        description: "Comprehensive customer view combining profile data with order history and engagement metrics"
        columns:
          - name: CUSTOMER_ID
            description: "Unique customer identifier from source Postgres system"
            data_type: INTEGER
          - name: FULL_NAME
            description: "Customer full name"
            data_type: VARCHAR
          - name: COMPANY_NAME
            description: "Customer company or organization name"
            data_type: VARCHAR
          - name: SEGMENT
            description: "Customer segment classification"
            data_type: VARCHAR
            sample_values: ["ENTERPRISE", "MID-MARKET", "SMB"]
          - name: REVENUE_TIER
            description: "Revenue-based tier classification"
            data_type: VARCHAR
            sample_values: ["ENTERPRISE", "MID-MARKET", "SMB"]
          - name: ANNUAL_REVENUE
            description: "Customer annual revenue in dollars"
            data_type: NUMBER
          - name: LIFETIME_VALUE
            description: "Total revenue from customer across all orders"
            data_type: NUMBER
            synonyms: ["LTV", "customer value", "total spend", "CLV"]
          - name: AVG_ORDER_VALUE
            description: "Average order amount"
            data_type: NUMBER
            synonyms: ["AOV", "basket size", "average purchase"]
          - name: TOTAL_ORDERS
            description: "Total number of orders placed by customer"
            data_type: INTEGER
            synonyms: ["order count", "number of orders", "purchases"]
          - name: DAYS_SINCE_LAST_ORDER
            description: "Days since customer last placed an order"
            data_type: INTEGER
